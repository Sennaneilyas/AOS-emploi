export function filterPosts(categoryId = "all", searchTerm = "") {
  let result = POSTS;
  if (categoryId !== "all") {
    result = result.filter((p) => p.category_id === categoryId);
  }
  if (searchTerm.trim()) {
    const q = searchTerm.trim().toLowerCase();
    result = result.filter(
      (p) =>
        p.title_ar.toLowerCase().includes(q) ||
        p.title_fr.toLowerCase().includes(q) ||
        p.excerpt_ar.toLowerCase().includes(q) ||
        p.excerpt_fr.toLowerCase().includes(q)
    );
  }
  return result;
}

export function paginatePosts(posts, page = 1) {
  const total = posts.length;
  const totalPages = Math.ceil(total / POSTS_PER_PAGE);
  const start = (page - 1) * POSTS_PER_PAGE;
  const items = posts.slice(start, start + POSTS_PER_PAGE);
  return { items, totalPages, total };
}

export function getPostBySlug(slug) {
  return POSTS.find((p) => p.slug === slug) ?? null;
}

export function getRelatedPosts(post, limit = 3) {
  return POSTS.filter(
    (p) => p.category_id === post.category_id && p.id !== post.id
  ).slice(0, limit);
}

export function getCategoryLabel(categoryId, lang = "ar") {
  const cat = CATEGORIES.find((c) => c.id === categoryId);
  return cat ? cat[`label_${lang}`] : categoryId;
}

